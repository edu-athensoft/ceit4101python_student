# APIs
# Application Programming Interface - python methods
str1 = "HELLO"
result = str1.lower()
print(result)

str2 = "hello"
result = str2.upper()
print(result)

# join()
s1 = 'a'
s2 = 'b'
s3 = '123'
list_a = [s1, s2, s3]
result = ''.join(list_a)
print(result)

# split()
str3 = "This is the biggest burger at Burger King"
print(str3.split())
print(len(str3.split()))


