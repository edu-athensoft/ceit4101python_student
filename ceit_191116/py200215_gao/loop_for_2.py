"""
range()
"""


# range(stop_number) - generating a sequence
print(range(10))
print(list(range(10)))
# length = stop_number - start_number


# range(start_number, stop_number) - generating a sequence
print(range(3, 10))
print(list(range(3, 10)))
# length = stop_number - start_number


# range(start_number, stop_number, step) - generating a sequence
print(range(3, 10, 2))
print(list(range(3, 10, 2)))
# length = stop_number - start_number

