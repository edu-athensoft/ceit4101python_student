import math

# Output: 3.141592653589793
print(math.pi)
print(math.e)

# Output: -1.0
print(math.cos(math.pi))

# Output: square root
print(math.sqrt(7))

# Output: 22026.465794806718
print(math.exp(10))

# Output: 3.0
print(math.log10(1000))

# Output: 1.1752011936438014
print(math.sinh(1))

# Output: 720
print(math.factorial(6))

# Output: gcd stands for Greatest Common Divisor
print(math.gcd(12,6))