# if statement

# case 1. Boolean literal
print("Start")

if True:
    print("execute if block.")
    print("execute if block 2.")

print("End")
print()

#case 2. Boolean variable
# bool_1 = True
bool_1 = False
print("Start")

if bool_1:
    print("execute if block.")
    print("execute if block 2.")

print("End")
print()

#case 3. comparision expression
test_expr1 = 3 > 2
print("Start")

if test_expr1:
    print("execute if block.")
    print("execute if block 2.")

print("End")
print()

#case 4. logic expression
test_expr1 = 3 > 2 and 2 > 1
print("Start")

if test_expr1:
    print("execute if block.")
    print("execute if block 2.")

print("End")
print()

