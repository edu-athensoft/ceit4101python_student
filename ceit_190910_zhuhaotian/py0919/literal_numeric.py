# integer

a = 0b1010  #Binary Literals
b = 100     #Decimal Literal
c = 0o310   #Octal Literal
d = 0x12c   #Hexadecimal Literal

print(a)
print(b)
print(c)
print(d)

# float
# Float Literal
float_1 = 10.5
float_2 = 1.5e2
float_3 = 1.5e-2

print(float_1)
print(float_2)
print(float_3)

# Complex Literal
x = 3.14j
print(x, x.imag, x.real)

x = 3 + 6j
print(x, x.real, x.imag)


