# string concatenation

str1 = 'Hello'
str2 ='World!'

# + operator
# using +
print('str1 + str2 = ', str1 + str2)
print(str1, str2)

# * operator
# using *
print('str1 * 3 =', str1 * 3)


# concatenate without whitespaces
str3 = 'Hello ''World!'
print(str3)

# concatenate with whitespaces
str3 = 'Hello '  'World!'
print(str3)

# concatenate spanning multiple-lines
str3 = ('Hello ' 
        'World!')
print(str3)


