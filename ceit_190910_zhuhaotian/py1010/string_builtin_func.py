# string built-in function

my_str = 'hello world'
print(my_str)

# len()
print("The length of the string is", len(my_str))

my_list= []
print("The length of the list is", len(my_list))

my_tuple= (1,)
print("The length of the tuple is", len(my_tuple))

my_set = {1, 2, 3}
print("The length of the set is", len(my_set))

my_dict = {'key1':10, 'key2':20, 'key3':'abc'}
print("The length of the dictionary is", len(my_dict))


# enumerate()
list_enumerate = list(enumerate(my_str))
print('list(enumerate(str) = ', list_enumerate)


