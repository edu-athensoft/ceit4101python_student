# string - accessing char

str = 'programiz'
print('str = ', str)

# accessing char in a string by index
# first character
print('str[0] = ', str[0])

#last character
print('str[-1] = ', str[-1])


# slicing - substring
# slicing 2nd to 5th character
print('str[1:5] = ', str[1:5])

#slicing 6th to 2nd last character
print('str[5:-2] = ', str[5:-2])
print('str[5:4] = ', str[5:4])    # empty string



