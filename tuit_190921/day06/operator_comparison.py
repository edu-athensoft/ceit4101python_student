# comparison operators

a = 9
b = 2

c = a > b
print("{} > {} is {}".format(a, b, c))
print(type(c))

c = a < b
print("{} < {} is {}".format(a, b, c))

c = a == b
print("{} == {} is {}".format(a, b, c))

c = a != b
print("{} != {} is {}".format(a, b, c))

c = a >= b
print("{} >= {} is {}".format(a, b, c))

c = a <= b
print("{} <= {} is {}".format(a, b, c))
