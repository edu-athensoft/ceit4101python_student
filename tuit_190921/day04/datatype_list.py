# list is an ordered sequence of items

# create a list
my_list = [1, 2, 3, 4, 5]
print(my_list)
print(type(my_list))

# access item of a list
print(my_list[0])
print(my_list[1])
print(my_list[2])
print(my_list[3])
print(my_list[4])

# list is mutable
my_list[0] = 10
print(my_list[0])
print(my_list)
