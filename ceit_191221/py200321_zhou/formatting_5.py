"""
Number formatting for signed numbers
"""

# show the + sign
print("{:+f} {:+f}".format(12.23, -12.23))

# show the - sign only
print("{:-f} {:-f}".format(+12.23, -12.23))

# show space for + sign
print("{: f} {: f}".format(12.23, -12.23))



